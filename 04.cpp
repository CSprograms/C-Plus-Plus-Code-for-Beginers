#include <iostream>

using namespace std;

int main()
{
    //Statements to print "Hello World!" in single line.
    cout<<"Hello ";
    cout<<"World!";

    cout<<endl;

    //Statement to print "Hello World!" in two line, using escape sequence.
    cout<<"Hello \nWorld!";

    cout<<endl;

    //Statement to print "Hello World!" in two line, using manipulator.
    cout<<"Hello "<<endl<<"World!";

    return 0;
}